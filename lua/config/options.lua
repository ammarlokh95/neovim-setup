opt.cursorline = true

opt.termguicolors = true
opt.background = "dark"
opt.signcolumn = "yes"

opt.backspace = { "indent", "eol", "start" }

opt.clipboard:append("unnamedplus")

opt.splitright = true

opt.splitbelow = true
